package com.fastcampus.de.java.clip8;

public class Logical {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!b);

        System.out.println((1>2) || (3>2));
    }
}
